public class Day2 {
    public static void main(String[]args){
        String nama = "Fiki";
        //Println itu untuk mencetak nilai yang dimasukkan dan membuat baris baru dibawahnya
        System.out.println("Nama Saya Fiki");
        //Printf untuk mencetak nilai yang dimasukkan juga, bedanya jika ingin membuat baris baru,
        //harus menggunakan %n, dan untuk pemanggilan variabel, jika variabelnya String, maka ketik %s
        System.out.printf("nama saya %s %n", nama);
        //Print hanya untuk mencetak dan tidak membuat baris baru
        System.out.print("Nama Saya Fiki");
    }
    
}
