public class Day4 {
    public static void main(String[]args){
        // Penggunaan Escape Sequence
        // \n untuk Membuat Baris baru
        System.out.print("Halo nama Saya\nfiki");
        // \t untuk membuat spasi/jarak horizontal
        System.out.println("Halo Nama saya\t Fiki");
        // \\ untuk membuat backslash
        System.out.println("Halo nama saya Rifki\\Fiki");
        // \" untuk membuat tanda kutip ganda
        System.out.println("Halo nama saya \"fiki\"");
        // \' untuk menampilkan tanda kutip tunggal
        System.out.println("Halo nama saya \'fiki\'");
        // \r untuk kembali keawal baris
        System.out.println("Halo nama saya \rfiki");
        // \b untuk menghapus satu karakter sebelum cursor
        System.out.println("Halo nama saya\bfiki");
        // \f untuk memajukan halaman,biasanya dipakai di printer
        System.out.println("Halaman 1 \fHalaman 2");
        // \xxxx untuk memasukkan unicode karaket
        System.out.println("Halo Nama Saya Fiki \uD83D\uDD25");

    }
}
