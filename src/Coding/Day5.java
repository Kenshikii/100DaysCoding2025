public class Day5 {
    public static void main(String[]arg){
        // pemanggilan tipe data bilangan bulat
        // Pemanggilan tipe data Integer, Nilainya Sampai 2 miliar
        int a = 100;
        //Pemanggilan tipe data byte , nilainya sampai 128
        byte b = 120;
        //Pemanggilan tipe data short, nilainya sampai 32000
        short c = 30000;
        //Pemanggilan tipe data long , nilai tidak terbatas, dan menggunakan l dibelakang angka
        long d = 20000000000000l;
        
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        
        
    }
}
